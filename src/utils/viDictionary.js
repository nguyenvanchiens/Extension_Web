/**
 * Split PascalCase/camelCase thành mảng từ
 * VD: "SupplierInteraction" -> ["Supplier", "Interaction"]
 */
export function splitPascalCase(str) {
  return str
    .replace(/([a-z])([A-Z])/g, '$1 $2')
    .replace(/([A-Z]+)([A-Z][a-z])/g, '$1 $2')
    .split(' ');
}

/**
 * Dịch 1 chuỗi PascalCase sang tiếng Việt
 * Ưu tiên match cụm dài trước, sau đó match từng từ
 */
export function translateToVietnamese(pascalStr) {
  const words = splitPascalCase(pascalStr);
  const lowerPhrase = words.join(' ').toLowerCase();

  // Thử match cả cụm trước
  if (dictionary[lowerPhrase]) {
    return dictionary[lowerPhrase];
  }

  // Greedy match: thử cụm dài nhất trước (4 -> 3 -> 2 -> 1 từ)
  const result = [];
  let i = 0;
  while (i < words.length) {
    let matched = false;
    // Thử match từ cụm dài nhất (max 5 từ) xuống ngắn nhất (2 từ)
    for (let len = Math.min(5, words.length - i); len >= 2; len--) {
      const phrase = words.slice(i, i + len).join(' ').toLowerCase();
      if (dictionary[phrase]) {
        result.push(dictionary[phrase]);
        i += len;
        matched = true;
        break;
      }
    }
    if (!matched) {
      const oneWord = words[i].toLowerCase();
      result.push(dictionary[oneWord] || words[i]);
      i++;
    }
  }

  return result.join(' ');
}

/**
 * Chuyển PascalCase thành English readable
 * VD: "SupplierInteraction" -> "Supplier Interaction"
 */
export function toEnglishReadable(pascalStr) {
  return splitPascalCase(pascalStr).join(' ');
}
